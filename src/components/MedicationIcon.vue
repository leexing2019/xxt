<script setup lang="ts">
import { computed } from 'vue'
import { getMedicationColor } from '@/utils/medication-icon'

const props = defineProps<{
  name: string                    // 药品名称
  form?: 'liquid' | 'capsule' | 'tablet'  // 剂型
  appearanceDesc?: string         // 外观描述（用于提取颜色）
  color?: string                  // 指定颜色（可选）
  size?: number                   // 图标大小（默认 80）
  className?: string              // CSS 类名
}>()

// 默认值
const size = props.size ?? 80
const className = props.className ?? 'medication-icon-svg'

// 计算属性：获取颜色（优先使用指定颜色，其次从描述提取，最后使用名称哈希）
const color = computed<string>(() => {
  if (props.color) return props.color
  return getMedicationColor(props.name, props.appearanceDesc)
})

// 变浅颜色（用于胶囊双色效果）
const lightColor = computed<string>(() => {
  return lightenColor(color.value, 30)
})

/**
 * 变浅颜色
 */
function lightenColor(hexColor: string, percent: number): string {
  const num = parseInt(hexColor.slice(1), 16)
  const amt = Math.round(2.55 * percent)
  const R = Math.min((num >> 16) + amt, 255)
  const G = Math.min((num >> 8 & 0x00FF) + amt, 255)
  const B = Math.min((num & 0x0000FF) + amt, 255)
  return '#' + (0x1000000 + R * 0x10000 + G * 0x100 + B).toString(16).slice(1)
}
